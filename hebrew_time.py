#!/usr/bin/env python3
# -*- coding: utf-8 -*-
# המרת שעה HH:MM למילים בעברית

_HOURS = {
    0: "שתים עשרה", 1: "אחת", 2: "שתיים", 3: "שלוש",
    4: "ארבע", 5: "חמש", 6: "שש", 7: "שבע",
    8: "שמונה", 9: "תשע", 10: "עשר", 11: "אחת עשרה",
    12: "שתים עשרה", 13: "שלוש עשרה", 14: "ארבע עשרה",
    15: "חמש עשרה", 16: "שש עשרה", 17: "שבע עשרה",
    18: "שמונה עשרה", 19: "תשע עשרה", 20: "עשרים",
    21: "עשרים ואחת", 22: "עשרים ושתיים", 23: "עשרים ושלוש",
}

_ONES = ["", "אחת", "שתיים", "שלוש", "ארבע", "חמש",
         "שש", "שבע", "שמונה", "תשע"]

_TENS = ["", "", "עשרים", "שלושים", "ארבעים", "חמישים"]

_TEENS = {
    10: "עשר", 11: "אחת עשרה", 12: "שתים עשרה",
    13: "שלוש עשרה", 14: "ארבע עשרה", 15: "חמש עשרה",
    16: "שש עשרה", 17: "שבע עשרה", 18: "שמונה עשרה", 19: "תשע עשרה",
}


def _minutes_he(m: int) -> str:
    if m == 0:
        return ""
    if m == 1:
        return "דקה"
    if 2 <= m <= 9:
        return f"{_ONES[m]} דקות"
    if 10 <= m <= 19:
        return f"{_TEENS[m]} דקות"
    tens = _TENS[m // 10]
    ones = _ONES[m % 10]
    if ones:
        return f"{tens} ו{ones} דקות"
    return f"{tens} דקות"


def time_to_hebrew(time_str: str) -> str:
    """ממיר '14:07' ל'ארבע עשרה ושבע דקות'"""
    try:
        h, m = map(int, time_str.strip().split(":"))
    except Exception:
        return time_str

    hour = _HOURS.get(h % 24, str(h))
    mins = _minutes_he(m)

    if not mins:
        return hour
    return f"{hour} ו{mins}"


if __name__ == "__main__":
    # בדיקה
    tests = ["14:07", "09:00", "12:30", "23:45", "00:01", "08:15", "20:00"]
    for t in tests:
        print(f"{t} → {time_to_hebrew(t)}")
